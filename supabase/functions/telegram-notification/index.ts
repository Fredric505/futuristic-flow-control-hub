
import { serve } from "https://deno.land/std@0.168.0/http/server.ts"

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
}

interface NotificationData {
  NotificationTitle?: string;
  NotificationMessage?: string;
  message?: string;
  sender?: string;
  response?: string;
}

serve(async (req) => {
  const timestamp = new Date().toISOString();
  console.log(`[${timestamp}] üöÄ INICIANDO PROCESAMIENTO DE NOTIFICACI√ìN`);
  console.log(`[${timestamp}] M√©todo: ${req.method}`);
  
  if (req.method === 'OPTIONS') {
    console.log('‚úÖ Manejando solicitud CORS preflight');
    return new Response(null, { headers: corsHeaders });
  }

  try {
    console.log('üîç Procesando datos de la notificaci√≥n...');
    
    let requestData: NotificationData;
    
    try {
      const body = await req.text();
      console.log('üìù Cuerpo de la solicitud:', body);
      console.log('üìã Content-Type:', req.headers.get('content-type'));
      
      if (!body || body.trim() === '') {
        console.log('‚ö†Ô∏è Cuerpo vac√≠o, usando datos de prueba');
        requestData = {
          NotificationTitle: 'N√∫mero de prueba',
          NotificationMessage: 'Mensaje de prueba masivo'
        };
      } else {
        try {
          requestData = JSON.parse(body);
          console.log('‚úÖ Parseado como JSON exitoso:', requestData);
        } catch (jsonError) {
          console.log('‚ö†Ô∏è Error JSON, intentando formato texto...');
          
          if (body.includes('{{NotificationTitle}}') || body.includes('{{NotificationMessage}}')) {
            console.log('‚ùå Variables IFTTT no reemplazadas');
            return new Response(
              JSON.stringify({ 
                success: false, 
                error: 'IFTTT template error',
                message: 'Variables IFTTT no reemplazadas correctamente.',
                received_body: body
              }), 
              { 
                headers: { ...corsHeaders, 'Content-Type': 'application/json' },
                status: 400
              }
            );
          }
          
          const lines = body.split('\n').map(line => line.trim()).filter(line => line.length > 0);
          console.log('üìÑ L√≠neas procesadas:', lines);
          
          if (lines.length >= 2) {
            requestData = {
              NotificationTitle: lines[0],
              NotificationMessage: lines[1]
            };
          } else if (lines.length === 1) {
            requestData = {
              NotificationTitle: 'N√∫mero desconocido',
              NotificationMessage: lines[0]
            };
          } else {
            throw new Error('No se pudo procesar el cuerpo de la solicitud');
          }
        }
      }
    } catch (parseError) {
      console.error('‚ùå Error procesando solicitud:', parseError);
      return new Response(
        JSON.stringify({ 
          success: false, 
          error: 'Formato de solicitud inv√°lido',
          message: 'Debe ser JSON o texto plano.',
          details: parseError.message
        }), 
        { 
          headers: { ...corsHeaders, 'Content-Type': 'application/json' },
          status: 400
        }
      );
    }
    
    console.log('üìä Datos de notificaci√≥n procesados:', requestData);

    let phoneNumber = '';
    let messageText = '';
    
    if (requestData.NotificationTitle || requestData.NotificationMessage) {
      phoneNumber = requestData.NotificationTitle || '';
      messageText = requestData.NotificationMessage || '';
    } else if (requestData.message) {
      messageText = requestData.message;
      phoneNumber = requestData.sender || '';
    }

    console.log('üéØ Datos extra√≠dos:', { phoneNumber, messageText });

    const supabaseUrl = Deno.env.get('SUPABASE_URL')!;
    const supabaseKey = Deno.env.get('SUPABASE_SERVICE_ROLE_KEY')!;
    
    console.log('üîó Conectando a Supabase...');
    const { createClient } = await import('https://esm.sh/@supabase/supabase-js@2');
    const supabase = createClient(supabaseUrl, supabaseKey);

    // Modo de prueba
    if (!phoneNumber || phoneNumber === 'N√∫mero de prueba' || phoneNumber === 'N√∫mero desconocido') {
      console.log('üß™ MODO PRUEBA DETECTADO');
      
      const authHeader = req.headers.get('authorization');
      let targetUserId = null;
      
      if (authHeader) {
        try {
          const token = authHeader.replace('Bearer ', '');
          const { data: { user }, error: userError } = await supabase.auth.getUser(token);
          
          if (!userError && user) {
            targetUserId = user.id;
            console.log('üë§ Usuario encontrado:', user.email);
          }
        } catch (tokenError) {
          console.log('‚ö†Ô∏è Error procesando token:', tokenError);
        }
      }
      
      let processQuery = supabase.from('processes').select('*');
      
      if (targetUserId) {
        processQuery = processQuery.eq('user_id', targetUserId);
        console.log('üéØ Buscando proceso para usuario espec√≠fico:', targetUserId);
      } else {
        console.log('üîç Buscando cualquier proceso disponible');
      }
      
      const { data: processes, error: queryError } = await processQuery.limit(1);

      if (queryError) {
        console.error('‚ùå Error en consulta:', queryError);
        return new Response(
          JSON.stringify({ 
            success: false, 
            error: 'Error en base de datos',
            details: queryError.message 
          }), 
          { 
            headers: { ...corsHeaders, 'Content-Type': 'application/json' },
            status: 500
          }
        );
      }

      if (!processes || processes.length === 0) {
        console.log('‚ùå No se encontraron procesos');
        return new Response(
          JSON.stringify({ 
            success: false, 
            error: 'No se encontraron procesos',
            message: 'No hay procesos disponibles para modo prueba'
          }), 
          { 
            headers: { ...corsHeaders, 'Content-Type': 'application/json' },
            status: 404
          }
        );
      }

      const process = processes[0];
      console.log('‚úÖ Usando proceso:', process.id, 'usuario:', process.user_id);
      return await sendNotificationToUser(process, phoneNumber || 'N√∫mero de prueba', messageText || 'Mensaje de prueba', supabase, true);
    }

    // Modo real - b√∫squeda exhaustiva
    console.log('üîç MODO REAL: Buscando proceso para:', phoneNumber);
    const matchedProcess = await findProcessByPhoneNumber(phoneNumber, supabase);

    if (!matchedProcess) {
      console.log('‚ùå No se encontr√≥ proceso para:', phoneNumber);
      return new Response(
        JSON.stringify({ 
          success: false, 
          error: 'Proceso no encontrado',
          message: `No se encontr√≥ proceso para: ${phoneNumber}`,
          phone_searched: phoneNumber
        }), 
        { 
          headers: { ...corsHeaders, 'Content-Type': 'application/json' },
          status: 404
        }
      );
    }

    console.log('‚úÖ Proceso encontrado:', matchedProcess.id, 'para usuario:', matchedProcess.user_id);
    return await sendNotificationToUser(matchedProcess, phoneNumber, messageText, supabase, false);

  } catch (error) {
    console.error('üí• ERROR CR√çTICO:', error);
    return new Response(
      JSON.stringify({ 
        success: false, 
        error: 'Error interno del servidor',
        details: error.message,
        timestamp: new Date().toISOString()
      }), 
      { 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 500
      }
    );
  }
});

async function findProcessByPhoneNumber(phoneNumber: string, supabase: any) {
  console.log('üîç Iniciando b√∫squeda exhaustiva para:', phoneNumber);
  
  const cleanPhoneNumber = phoneNumber.replace(/[\s\-\(\)]/g, '');
  console.log('üßπ N√∫mero limpio:', cleanPhoneNumber);

  // Patrones de b√∫squeda m√°s exhaustivos
  const searchPatterns = [
    phoneNumber,                                    // Original
    cleanPhoneNumber,                               // Limpio
    cleanPhoneNumber.startsWith('+') ? cleanPhoneNumber : `+${cleanPhoneNumber}`,
    cleanPhoneNumber.replace('+', ''),              // Sin +
    cleanPhoneNumber.replace(/^\+505/, ''),         // Sin c√≥digo Nicaragua
    cleanPhoneNumber.replace(/^\+52/, ''),          // Sin c√≥digo M√©xico
    cleanPhoneNumber.replace(/^\+57/, ''),          // Sin c√≥digo Colombia
    cleanPhoneNumber.replace(/^\+593/, ''),         // Sin c√≥digo Ecuador
    cleanPhoneNumber.replace(/^\+591/, ''),         // Sin c√≥digo Bolivia
    cleanPhoneNumber.replace(/^\+56/, ''),          // Sin c√≥digo Chile
    cleanPhoneNumber.replace(/^\+51/, ''),          // Sin c√≥digo Per√∫
    cleanPhoneNumber.replace(/^\+506/, ''),         // Sin c√≥digo Costa Rica
    cleanPhoneNumber.replace(/^\+502/, ''),         // Sin c√≥digo Guatemala
    cleanPhoneNumber.replace(/^\+503/, ''),         // Sin c√≥digo El Salvador
    cleanPhoneNumber.replace(/^\+504/, ''),         // Sin c√≥digo Honduras
    cleanPhoneNumber.replace(/^\+507/, ''),         // Sin c√≥digo Panam√°
    cleanPhoneNumber.replace(/^505/, ''),           // Sin 505
    cleanPhoneNumber.replace(/^52/, ''),            // Sin 52
    cleanPhoneNumber.replace(/^57/, ''),            // Sin 57
    cleanPhoneNumber.replace(/^593/, ''),           // Sin 593
    cleanPhoneNumber.replace(/^591/, ''),           // Sin 591
    cleanPhoneNumber.replace(/^56/, ''),            // Sin 56
    cleanPhoneNumber.replace(/^51/, ''),            // Sin 51
    cleanPhoneNumber.replace(/^506/, ''),           // Sin 506
    cleanPhoneNumber.replace(/^502/, ''),           // Sin 502
    cleanPhoneNumber.replace(/^503/, ''),           // Sin 503
    cleanPhoneNumber.replace(/^504/, ''),           // Sin 504
    cleanPhoneNumber.replace(/^507/, ''),           // Sin 507
    cleanPhoneNumber.slice(-8),                     // √öltimos 8 d√≠gitos
    cleanPhoneNumber.slice(-7),                     // √öltimos 7 d√≠gitos
    cleanPhoneNumber.slice(-6),                     // √öltimos 6 d√≠gitos
    cleanPhoneNumber.slice(-5),                     // √öltimos 5 d√≠gitos
  ];

  const uniquePatterns = [...new Set(searchPatterns)].filter(p => p && p.length > 0);
  console.log('üéØ Patrones de b√∫squeda:', uniquePatterns);

  // Obtener TODOS los procesos
  const { data: allProcesses, error: allError } = await supabase
    .from('processes')
    .select('*');

  if (allError) {
    console.error('‚ùå Error obteniendo procesos:', allError);
    return null;
  }

  if (!allProcesses || allProcesses.length === 0) {
    console.log('‚ùå No hay procesos en la base de datos');
    return null;
  }

  console.log(`üîç Verificando ${allProcesses.length} procesos`);

  // B√∫squeda exhaustiva con m√∫ltiples estrategias
  for (const proc of allProcesses) {
    const fullNumber = `${proc.country_code}${proc.phone_number}`;
    const fullNumberClean = fullNumber.replace(/[\s\-\(\)]/g, '');
    const phoneOnly = proc.phone_number.replace(/[\s\-\(\)]/g, '');
    
    console.log(`üîç Verificando proceso ${proc.id}:`);
    console.log(`   üì± Tel√©fono almacenado: "${proc.phone_number}"`);
    console.log(`   üåç C√≥digo pa√≠s: "${proc.country_code}"`);
    console.log(`   üî¢ N√∫mero completo: "${fullNumber}"`);
    console.log(`   üßπ N√∫mero completo limpio: "${fullNumberClean}"`);
    
    // M√∫ltiples estrategias de matching
    for (const pattern of uniquePatterns) {
      // Estrategia 1: Coincidencia exacta
      if (fullNumberClean === pattern || 
          fullNumber === pattern ||
          proc.phone_number === pattern ||
          phoneOnly === pattern) {
        console.log(`‚úÖ COINCIDENCIA EXACTA! Patr√≥n: "${pattern}" = proceso ${proc.id}`);
        return proc;
      }
      
      // Estrategia 2: Coincidencia al final
      if (fullNumberClean.endsWith(pattern) || 
          pattern.endsWith(fullNumberClean) ||
          fullNumberClean.endsWith(phoneOnly) ||
          pattern.endsWith(phoneOnly)) {
        console.log(`‚úÖ COINCIDENCIA AL FINAL! Patr√≥n: "${pattern}" = proceso ${proc.id}`);
        return proc;
      }
      
      // Estrategia 3: Coincidencia sin c√≥digo de pa√≠s
      if (phoneOnly === pattern || 
          pattern === phoneOnly) {
        console.log(`‚úÖ COINCIDENCIA SIN C√ìDIGO! Patr√≥n: "${pattern}" = proceso ${proc.id}`);
        return proc;
      }
      
      // Estrategia 4: Coincidencia flexible (contiene)
      if (fullNumberClean.includes(pattern) || 
          pattern.includes(fullNumberClean) ||
          fullNumberClean.includes(phoneOnly) ||
          pattern.includes(phoneOnly)) {
        console.log(`‚úÖ COINCIDENCIA FLEXIBLE! Patr√≥n: "${pattern}" = proceso ${proc.id}`);
        return proc;
      }
    }
  }

  console.log('‚ùå No se encontr√≥ coincidencia despu√©s de verificar todos los patrones');
  
  // Log de debug para ayudar a diagnosticar
  console.log('üìä RESUMEN DE B√öSQUEDA:');
  console.log('   üîç N√∫mero buscado:', phoneNumber);
  console.log('   üßπ N√∫mero limpio:', cleanPhoneNumber);
  console.log('   üì± Procesos en DB:', allProcesses.length);
  console.log('   üéØ Patrones probados:', uniquePatterns.length);
  
  return null;
}

async function sendNotificationToUser(process: any, phoneNumber: string, messageText: string, supabase: any, isTestMode: boolean) {
  console.log(`üîî ENVIANDO NOTIFICACI√ìN:`);
  console.log(`   üë§ Usuario: ${process.user_id}`);
  console.log(`   üìã Proceso: ${process.id}`);
  console.log(`   üß™ Modo prueba: ${isTestMode}`);

  // Obtener configuraci√≥n del usuario
  const { data: profile, error: profileError } = await supabase
    .from('profiles')
    .select('telegram_bot_token, telegram_chat_id, email')
    .eq('id', process.user_id)
    .single();

  if (profileError) {
    console.error('‚ùå Error obteniendo perfil:', profileError);
    return new Response(
      JSON.stringify({ 
        success: false, 
        error: 'Error de perfil',
        message: `Error buscando perfil: ${process.user_id}`,
        details: profileError.message
      }), 
      { 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 500
      }
    );
  }

  if (!profile) {
    console.error('‚ùå Perfil no encontrado:', process.user_id);
    return new Response(
      JSON.stringify({ 
        success: false, 
        error: 'Perfil no encontrado',
        message: `No existe perfil para: ${process.user_id}`
      }), 
      { 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 404
      }
    );
  }

  console.log(`üìß Perfil encontrado: ${profile.email}`);
  console.log(`ü§ñ Bot token: ${profile.telegram_bot_token ? 'CONFIGURADO' : 'NO CONFIGURADO'}`);
  console.log(`üí¨ Chat ID: ${profile.telegram_chat_id ? 'CONFIGURADO' : 'NO CONFIGURADO'}`);

  if (!profile.telegram_bot_token || !profile.telegram_chat_id) {
    console.log('‚ùå Configuraci√≥n Telegram incompleta');
    return new Response(
      JSON.stringify({ 
        success: false, 
        error: 'Telegram no configurado',
        message: `Usuario ${profile.email} no tiene Telegram configurado`,
        user_id: process.user_id,
        user_email: profile.email,
        missing_config: {
          bot_token: !profile.telegram_bot_token,
          chat_id: !profile.telegram_chat_id
        }
      }), 
      { 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 400
      }
    );
  }

  // Crear mensaje personalizado
  const notificationMessage = `üîî ${isTestMode ? 'PRUEBA - ' : ''}Alerta WhatsApp

üë©üèΩ‚Äçüíª Servidor Astro${isTestMode ? ' - MODO PRUEBA' : ''}

üìä PROCESO:
üë§ Cliente: ${process.client_name}
üì± Modelo: ${process.iphone_model}
üìû IMEI: ${process.imei}
üî¢ Serie: ${process.serial_number}
${process.owner_name ? `üë• Propietario: ${process.owner_name}` : ''}

üìû De: ${phoneNumber}
üì• Mensaje: ${messageText}

ü§ñ Bot Astro üü¢
‚è∞ ${new Date().toLocaleString('es-ES')}

${isTestMode ? '‚ö†Ô∏è MENSAJE DE PRUEBA' : ''}`;

  console.log(`üöÄ Enviando a Telegram para: ${profile.email}`);
  
  const telegramUrl = `https://api.telegram.org/bot${profile.telegram_bot_token}/sendMessage`;
  
  try {
    const telegramResponse = await fetch(telegramUrl, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        chat_id: profile.telegram_chat_id,
        text: notificationMessage,
        parse_mode: 'HTML'
      }),
    });

    const telegramResult = await telegramResponse.json();
    console.log(`üì° Telegram API status: ${telegramResponse.status}`);
    console.log('üì° Telegram respuesta:', telegramResult);
    
    if (!telegramResponse.ok) {
      console.error('‚ùå Error Telegram API:', telegramResult);
      return new Response(
        JSON.stringify({ 
          success: false, 
          error: 'Error enviando a Telegram',
          details: telegramResult,
          user_id: process.user_id,
          user_email: profile.email,
          telegram_status: telegramResponse.status
        }), 
        { 
          headers: { ...corsHeaders, 'Content-Type': 'application/json' },
          status: 500
        }
      );
    }

    console.log(`‚úÖ NOTIFICACI√ìN ENVIADA EXITOSAMENTE a: ${profile.email}`);

    return new Response(
      JSON.stringify({ 
        success: true, 
        message: isTestMode ? 'Prueba enviada exitosamente' : 'Notificaci√≥n enviada exitosamente',
        process_id: process.id,
        user_id: process.user_id,
        user_email: profile.email,
        phone_number: phoneNumber,
        message_content: messageText,
        test_mode: isTestMode,
        telegram_message_id: telegramResult.result?.message_id,
        timestamp: new Date().toISOString()
      }), 
      { 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 200
      }
    );

  } catch (telegramError) {
    console.error('‚ùå Error conexi√≥n Telegram:', telegramError);
    return new Response(
      JSON.stringify({ 
        success: false, 
        error: 'Error conexi√≥n Telegram',
        details: telegramError.message,
        user_id: process.user_id,
        user_email: profile.email
      }), 
      { 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 500
      }
    );
  }
}
